function drawMapGrid(pNode) --for debug
	local dNode=cc.DrawNode:create()
	dNode:setAnchorPoint(0,0)
	pNode:addChild(dNode,-10)
	for i=1,ConfigMap.Width+1 do
		local x=(i-1)*ConfigMap.mapGridW
		dNode:drawLine(cc.p(x,0),cc.p(x,ConfigMap.totalHeight),cc.c4f(0,1,0,1))
	end
	for i=1,ConfigMap.Height+1 do
		local y=(i-1)*ConfigMap.mapGridH
		dNode:drawLine(cc.p(0,y),cc.p(ConfigMap.totalWidth,y),cc.c4f(0,1,0,1))
	end
	local w=ConfigMap.mapGridW
	local h=ConfigMap.mapGridH 
	for i=0,ConfigMap.Height-1 do
		for j=0,ConfigMap.Width-1 do
			if 0==ConfigMap.data[i*ConfigMap.Width+j+1] then
				local lb=cc.p(j*w,i*h)
				local rt=cc.p(j*w+w,i*h+h)
				dNode:drawSolidRect(lb,rt,cc.c4f(1,0,0,1))
			end
		end
	end
end